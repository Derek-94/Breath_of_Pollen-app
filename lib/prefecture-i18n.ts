/**
 * Returns a localized display name for a stored location key.
 * - KR compound key ('서울특별시_강남구'): returns Katakana (ja), English (en), Korean district (ko/default)
 * - JP prefecture key: returns Japanese (ja), Korean (ko), romanized (en/default)
 * - KR 시/도 key: returns Katakana (ja), English (en), Korean (ko/default)
 */
export function localizeLocationName(name: string, language: string): string {
  if (name.includes('_')) {
    if (language === 'ja') return KOREA_SIGUNGU_KATAKANA[name] ?? getSigunguDisplayName(name)
    if (language === 'en') return KOREA_SIGUNGU_EN[name] ?? getSigunguDisplayName(name)
    return getSigunguDisplayName(name)
  }
  if (language === 'ja') return KOREA_KATAKANA[name] ?? name
  if (language === 'ko') {
    if (PREFECTURE_KOREAN[name]) return PREFECTURE_KOREAN[name]
    if (KOREA_ROMANIZED[name]) return KOREA_ROMANIZED[name]
    return name
  }
  if (PREFECTURE_ROMANIZED[name]) return PREFECTURE_ROMANIZED[name]
  if (language === 'en' && KOREA_ROMANIZED[name]) return KOREA_ROMANIZED[name]
  return name
}
